namespace Example\UserRegistrationBundle\Entity;

use PHPMentors\DomainKata\Entity\SingleValueInterface;

class ActivationKey implements SingleValueInterface
{
    /**
     * @var string
     */
    private $value;

    /**
     * @param string $value
     */
    public function __construct($value)
    {
        $this->value = $value;
    }

    /**
     * {@inheritDoc}
     *
     * @return string
     */
    public function getValue()
    {
        return $this->value;
    }
}
